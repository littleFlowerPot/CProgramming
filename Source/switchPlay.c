#include<stdio.h>

int main(void)
{
    char trafficLightColor = '0';


    switch(trafficLightColor){
	case 'r':
	    printf("Stop Driving \n");
	case 'y':
	    printf("Slow down if within 30m\n");
	case 'g':
	    printf("Go go go!\n");
	default:
	    printf("TrafficLight has malfunctioned\n");

}









}
